from twoD2 import solve


def test_document1():
    answer = solve([
        "x......x",
        "....x...",
        "........",
        "........",
        "........",
        "........",
        "........",
        ".......x"])
    
    assert(answer == [
        "x------x",
        ".\..x..|",
        "..\....|",
        "...\...|",
        "....\..|",
        ".....\.|",
        "......\|",
        ".......x"])
    
def test_document2():
    answer = solve([
        ".x...x..",
        "........",
        ".x.x....",
        "........",
        "...x....",
        "........",
        "........",
        "........"])

    assert(answer == [
        ".x---x..",
        ".|\./...",
        ".x-x....",
        "..\|....",
        "...x....",
        "........",
        "........",
        "........"])
    
