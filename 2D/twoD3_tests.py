from twoD3 import solve

def test_document1():
    assert(solve(10, [[1,2], [3,4], [5,6], [7,8], [9,1], [2,3]]) == [
['5'],
['5', 'f',],
['3', 'f'],
['2'],
['f'],
['0', '1'],
['f:', '1', '2', '4']]
)
    
def test_document2():
    assert(solve(8, [[4,5], [5,6], [6, 7], [2,3]]) == [
['1', 'f'],
['0', '2'],
['1'],
['f'],
['f:', '0', '3']]
)
    
def test_document3():
    assert(solve(6, [[1,4], [5,3], [5,2], [2,1], [4,4], [3,1]]) == [['f'],
['2', '4'],
['1'],
['5'],
['1', 'f'],
['3', 'f'],
['f:', '0', '4', '5']]
)